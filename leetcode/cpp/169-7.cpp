class Solution {
public:
    // Bits Manipulation
    // The bits in the majority are just the majority bits of all numbers.
    // code from Jianchao Li's solution in the problem disscuss forum
    int majorityElement(vector<int>& nums) {
        int majority = 0;
        for (unsigned int i = 0, mask = 1; i < 32; i++, mask <<= 1) {
            int bits = 0;
            for (int num : nums) {
                if (num & mask) {
                    bits++;
                }
            }
            if (bits > nums.size() / 2) {
                majority |= mask;
            }
        }
        return majority;
    }
};